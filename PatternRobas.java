public class PatternRobas {
    public static void main(String[]vijay){
        for (int i=0;i<5;i++){
            for (int k=5;k>i;k--) {
                System.out.print(" ");}
            for (int j=0;j<5;j++) {
                    System.out.print("*");
            }

            System.out.println(" ");
        }
    }
}
